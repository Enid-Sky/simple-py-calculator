#################
#Basic Functions#
#################

# ADDITION

def add(x, y):
    return x + y

# SUBTRACTION


def sub(x, y):
    return x - y

# MULTIPLICATION


def mult(x, y):
    return x * y

# DIVISION


def div(x, y):
    return x / y

# EXPONENT


def expo(x, y):
    return x ** y
